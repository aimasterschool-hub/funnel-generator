# ── 生成・キャッシュ ──────────────────────────────────────────────────────

def generate_outline(script: str, script_path: str, funnel_type: str = "") -> str:
    """台本テキストからLP生成用の骨子を生成して保存・返す"""
    save_path = resolve_outline_path(script_path)

    category = _funnel_category(funnel_type)
    if category == "video_funnel":
        prompt = _prompt_video_funnel(script)
    elif category == "optin":
        prompt = _prompt_optin(script)
    elif category == "sales_lp":
        prompt = _prompt_sales_lp(script)
    else:
        # フォールバック: 動画ファネル用プロンプトを使用
        prompt = _prompt_video_funnel(script)

    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=4000,
        messages=[{"role": "user", "content": prompt}],
    )

    outline = response.content[0].text.strip()
    save_path.write_text(outline, encoding="utf-8")
    return outline


def get_outline(script: str, script_path: str, funnel_type: str = "") -> "tuple[str, bool]":
    """
    骨子を取得する。既存なら読み込み、なければ生成して保存。
    Returns: (骨子テキスト, キャッシュから読んだか)
    """
    existing = load_outline(script_path)
    if existing:
        return existing, True
    outline = generate_outline(script, script_path, funnel_type)
    return outline, False
